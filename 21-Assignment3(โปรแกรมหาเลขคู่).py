#หาเลขคู่ / คี

'''
หลักการคิดคือ การป้อนเลขที่เป็นจำนวนเต็มลงไป แล้วทำการตั้งเงื่อนไขโดยการหารด้วยเลข 2 
ถ้าลงตัวจะเป็นเลขคู่
ุถ้าเหลือเศษจะเป็นเลขคี่
'''

number=int(input("ป้อนตัวเลขของคุณ :"))

#ถ้าหาร2ลงตัว => คู่
if number % 2==0 :
    print("เลขของคุณ :","เป็นเลขคู่")
else :
    print("เลขของคุณ :","เป็นเลขคี่")
print("จบโปรแกรม")